public class ElseIf 
{
  public static void main(String[] args)
  {
    int a = 35;
    int b = 35;
    if (a > b) 
    {
      System.out.println("a is greater than b");
    }
    else if(a<b)
    {
      System.out.println("a is smaller than b");
    }
    else
    {
      System.out.println("both values are equal");
    }
  }
}
