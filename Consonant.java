public class Consonant {
    public static void main(String[] args) {
        char ab = 'b';
        switch (ab) {
            case 'a':
                    System.out.println(ab + " is vowel");
                    break;
            case 'e':
                    System.out.println(ab + " is vowel");
                    break;
            case 'i':
                    System.out.println(ab + " is vowel");
                    break;
            case 'o':
                    System.out.println(ab + " is vowel");
                    break;
            case 'u':
                    System.out.println(ab + " is vowel");
                    break;
            default:
                    System.out.println(ab + " is consonant");
        }
    }
}
